package Practiceprograms;
import java.util.*;
public class Arraydupicate {
public static void main (String args[]) {
	Scanner sc=new Scanner (System.in);
	int n=sc.nextInt();
	int arr []=new int [n];
	for(int i=0;i<n;i++) {
		arr[i]=sc.nextInt();
	}
	for(int i=0;i<n;i++) {
		boolean isduplicate=false;
		for(int j=0;j<i;j++) {
			if(arr[i]==arr[j]){
				isduplicate=true;
				break;
			}
		}
	
	if(!isduplicate) {
		System.out.print(arr[i]+" ");
	}
		
}
}
}
